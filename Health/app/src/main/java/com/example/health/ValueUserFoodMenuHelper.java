package com.example.health;

public class ValueUserFoodMenuHelper {
    private int dayOfWeek;
    private int eating;
    private int weight;
    private String productName;
    private double productCalories;
    private double productProtein;
    private double productFat;
    private double productCarbohyd;

    private int dayOfMonth;
    private int month;
    private int year;

    public int getDayOfWeek() {
        return dayOfWeek;
    }

    public void setDayOfWeek(int dayOfWeek) {
        this.dayOfWeek = dayOfWeek;
    }

    public int getEating() {
        return eating;
    }

    public void setEating(int eating) {
        this.eating = eating;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public double getProductCalories() {
        return productCalories;
    }

    public void setProductCalories(double productCalories) {
        this.productCalories = productCalories;
    }

    public double getProductProtein() {
        return productProtein;
    }

    public void setProductProtein(double productProtein) {
        this.productProtein = productProtein;
    }

    public double getProductFat() {
        return productFat;
    }

    public void setProductFat(double productFat) {
        this.productFat = productFat;
    }

    public double getProductCarbohyd() {
        return productCarbohyd;
    }

    public void setProductCarbohyd(double productCarbohyd) {
        this.productCarbohyd = productCarbohyd;
    }

    public int getDayOfMonth() {
        return dayOfMonth;
    }

    public void setDayOfMonth(int dayOfMonth) {
        this.dayOfMonth = dayOfMonth;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
